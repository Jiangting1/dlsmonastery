<template>
  <div>
    <head-tab active="carrierShopList"></head-tab>
    <div>
      <el-row>
        <el-button type="primary" @click="itemAdd" icon="plus" v-permit="'api:carrierShop:edit'">{{$t('carrierShopList.add')}}</el-button>
        <el-button type="primary"@click="formVisible = true" icon="search" v-permit="'api:carrierShop:view'">{{$t('carrierShopList.filter')}}</el-button>
        <span v-html="searchText"></span>
      </el-row>
      <search-dialog @enter="search()" :show="formVisible" @hide="formVisible=false" :title="$t('carrierShopList.filter')" v-model="formVisible" size="tiny" class="search-form" z-index="1500" ref="searchDialog">
        <el-form :model="formData" :label-width="formLabelWidth">
          <el-row :gutter="4">
            <el-col :span="24">
              <el-form-item :label="$t('carrierShopList.name')" >
                <el-input v-model="formData.name" auto-complete="off" :placeholder="$t('carrierShopList.likeSearch')"></el-input>
              </el-form-item>
            </el-col>
          </el-row>
        </el-form>
        <div slot="footer" class="dialog-footer">
          <el-button type="primary" @click="search()">确定</el-button>
        </div>
      </search-dialog>
      <el-table :data="page.content" :height="pageHeight" style="margin-top:5px;" v-loading="pageLoading" :element-loading-text="$t('carrierShopList.loading')" @sort-change="sortChange" stripe border>
        <el-table-column fixed prop="name" :label="$t('carrierShopList.name')" sortable width="300"></el-table-column>
        <el-table-column prop="code" :label="$t('carrierShopList.code')" sortable></el-table-column>
        <el-table-column prop="createdByName" :label="$t('carrierShopList.createdBy')"></el-table-column>
        <el-table-column prop="remarks" :label="$t('carrierShopList.remarks')"></el-table-column>
        <el-table-column prop="locked" :label="$t('carrierShopList.locked')" width="120">
          <template scope="scope">
            <el-tag :type="scope.row.locked ? 'primary' : 'danger'">{{scope.row.locked | bool2str}}</el-tag>
          </template>
        </el-table-column>
        <el-table-column fixed="right" :label="$t('carrierShopList.operation')" width="140">
          <template scope="scope">
            <el-button size="small" @click.native="itemAction(scope.row.id,'edit')" v-permit="'api:carrierShop:edit'">{{$t('carrierShopList.edit')}}</el-button>
            <el-button size="small" @click.native="itemAction(scope.row.id,'delete')" v-permit="'api:carrierShop:delete'">{{$t('carrierShopList.delete')}}</el-button>
          </template>
        </el-table-column>
      </el-table>
      <pageable :page="page" v-on:pageChange="pageChange"></pageable>
    </div>
  </div>
</template>
<script>
  export default{
    data() {
      return {
        page:{},
        searchText:"",
        formData:{
          extra:{}
        },
        initPromise:{},
        formVisible: false,
        pageLoading: false,
        formLabelWidth:'28%'
      };
    },
    methods: {
      setSearchText() {
        this.$nextTick(function () {
          this.searchText = util.getSearchText(this.$refs.searchDialog);
        })
      },
      pageRequest() {
        this.pageLoading = true;
        this.setSearchText();
        var submitData = util.deleteExtra(this.formData);
        axios.get('/api/ws/future/api/carrierShop?'+qs.stringify(submitData)).then((response) => {
          this.page = response.data;
          this.pageLoading = false;
        })
      },pageChange(pageNumber,pageSize) {
        this.formData.page = pageNumber;
        this.formData.size = pageSize;
        this.pageRequest();
      },sortChange(column) {
        this.formData.sort=util.getSort(column);
        this.formData.page=0;
        this.pageRequest();
      },search() {
        this.formVisible = false;
        this.pageRequest();
      },itemAdd(){
        this.$router.push({ name: 'carrierShopForm'})
      },itemAction:function(id,action){
        if(action=="edit") {
          this.$router.push({ name: 'carrierShopForm', query: { id: id }})
        } else if(action=="delete") {
          util.confirmBeforeDelRecord(this).then(() => {
            axios.get('/api/ws/future/api/carrierShop/delete',{params:{id:id}}).then((response) =>{
              this.$message(response.data.message);
              this.pageRequest();
            });
          }).catch(()=>{});
        }
      }
    },created () {
      var that = this;
      that.pageHeight = 0.74*window.innerHeight;
      that.initPromise = axios.get('/api/ws/future/api/carrierShop/getQuery').then((response) =>{
        that.formData=response.data;
      });
    },activated(){
      this.initPromise.then(()=>{
        this.pageRequest();
      });
    }
  };
</script>

